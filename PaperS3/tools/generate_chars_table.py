def generate_chars_table(count, output_path):
    """生成指定数量的常用汉字字符表"""
    
    # 使用预设的不同级别字符集
    if count <= 500:
        chars = COMMON_CHINESE_500
    elif count <= 3500:
        chars = COMMON_CHINESE_3500
    else:
        chars = COMMON_CHINESE_3500
    
    # 去重并保持顺序
    seen = set()
    unique_chars = []
    for ch in chars:
        if ch not in seen and not ch.isspace():
            seen.add(ch)
            unique_chars.append(ch)
            if len(unique_chars) >= count:
                break
    
    # 写入文件，每行50个字符
    with open(output_path, 'w', encoding='utf-8') as f:
        f.write(f"# 常用汉字 {len(unique_chars)} 字\n")
        f.write("# 用于生成 Vink-PaperS3 固件字库\n\n")
        
        for i in range(0, len(unique_chars), 50):
            line = ''.join(unique_chars[i:i+50])
            f.write(line + '\n')
        
        # 添加常用标点
        f.write("\n# 常用标点符号\n")
        f.write('，。！？、；：""''（）《》【】—…·\n')
        
        # 添加英文字母和数字
        f.write("\n# 英文字母\n")
        f.write('abcdefghijklmnopqrstuvwxyz\n')
        f.write('ABCDEFGHIJKLMNOPQRSTUVWXYZ\n')
        f.write("\n# 数字\n")
        f.write('0123456789\n')
    
    print(f"已生成 {len(unique_chars)} 个常用汉字的字符表")
    print(f"保存至: {output_path}")
    return len(unique_chars)

def main():
    parser = argparse.ArgumentParser(description='生成常用汉字字符表')
    parser.add_argument('--count', '-n', type=int, default=500,
                        help='字符数量（默认500，可选3500）')
    parser.add_argument('--output', '-o', default='chars_common.txt',
                        help='输出文件名')
    
    args = parser.parse_args()
    generate_chars_table(args.count, args.output)

if __name__ == '__main__':
    main()
